                ],
                [
                    'name'  => __('All'),
                    'class' => 'active'
                ],
            ],
            'page_title'         => __("Manage Visas"),
        ];
        return view('Visa::frontend.manageVisa.index', $data);
    }

    public function recovery(Request $request)
