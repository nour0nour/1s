            ],
            'page_title'         => __("Recovery Visas"),
        ];
        return view('Visa::frontend.manageVisa.index', $data);
    }

    public function restore($id)
    {
        $this->checkPermission('visa_delete');
        $user_id = Auth::id();
        $query = $this->visaClass::onlyTrashed()->where("author_id", $user_id)->where("id", $id)->first();
        if(!empty($query)){
            $query->restore();
            event(new UpdatedServiceEvent($query));

        }
        return redirect(route('visa.vendor.recovery'))->with('success', __('Restore visa success!'));
    }

